print("Basic array for:");
for i in [1, 2, 3, 4, 5]:
    print(i)

print("\nBasic range for:");
for i in range(6):
    print(i)

print("\nBasic string for:");
name = "Cristina";
for char in name:
    print(char);

print("\nBasic list for:");
list = ["Cristina", "Lari", "Marta"];
for item in list:
    print(item);
